
import UIKit

extension UIFont {

    class func helveticaNeue(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "Helvetica Neue", size: fontSize)!
    }

    class func helveticaNeueMedium(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Medium", size: fontSize)!
    }

}
